        private static void SuspendProcess(int pid)
        {
            var process = Process.GetProcessById(pid);

            if (process.ProcessName == string.Empty)
            {
                return;
            }

            foreach (ProcessThread pT in process.Threads)
            {
                IntPtr pOpenThread = OpenThread(ThreadAccess.SUSPEND_RESUME, false, (uint)pT.Id);
                if (pOpenThread == IntPtr.Zero)
                {
                    continue;
                }
                SuspendThread(pOpenThread);
                CloseHandle(pOpenThread);
            }
        }

        public static void ResumeProcess(int pid)
        {
            var process = Process.GetProcessById(pid);

            if (process.ProcessName == string.Empty)
            {
                return;
            }

            foreach (ProcessThread pT in process.Threads)
            {
                IntPtr pOpenThread = OpenThread(ThreadAccess.SUSPEND_RESUME, false, (uint)pT.Id);

                if (pOpenThread == IntPtr.Zero)
                {
                    continue;
                }

                var suspendCount = 0;
                do
                {
                    suspendCount = ResumeThread(pOpenThread);
                } while (suspendCount > 0);

                CloseHandle(pOpenThread);
            }
        }

        public static int WriteMemory(int pid, int address, long valueToWrite)
        {
            var hProc = OpenProcess(ProcessAccessFlags.All, false, pid);
            var bytesToWrite = new byte[] { (byte)valueToWrite };

            int noOfBytesWritten = 0;
            WriteProcessMemory(hProc, new IntPtr(address), bytesToWrite, (UInt32)bytesToWrite.LongLength, out noOfBytesWritten);

            CloseHandle(hProc);
            return noOfBytesWritten;
        }

        public static int CreateProcessWrapper(string appPath, string cliArgs, bool suspended)
        {
            bool returnSuccessState = false;
            const uint NORMAL_PRIORITY_CLASS = 0x0020;
            const uint CREATE_SUSPENDED = 0x4;

            PROCESS_INFORMATION pInfo = new PROCESS_INFORMATION();
            STARTUPINFO sInfo = new STARTUPINFO();
            SECURITY_ATTRIBUTES pSec = new SECURITY_ATTRIBUTES();
            SECURITY_ATTRIBUTES tSec = new SECURITY_ATTRIBUTES();
            pSec.nLength = Marshal.SizeOf(pSec);
            tSec.nLength = Marshal.SizeOf(tSec);
            if (suspended)
            {
                returnSuccessState = CreateProcess(appPath, cliArgs, ref pSec, ref tSec, false, NORMAL_PRIORITY_CLASS | CREATE_SUSPENDED, IntPtr.Zero, null, ref sInfo, out pInfo);
            }
            else
            {
                returnSuccessState = CreateProcess(appPath, cliArgs, ref pSec, ref tSec, false, NORMAL_PRIORITY_CLASS, IntPtr.Zero, null, ref sInfo, out pInfo);
            }
            return pInfo.dwProcessId;
        }

        public static void InjectDLL(int processId, string dllPath)
        {
            IntPtr procHandle = OpenProcess(ProcessAccessFlags.All, false, processId);
            IntPtr loadLibraryAddr = GetProcAddress(GetModuleHandle("kernel32.dll"), "LoadLibraryA");

            // alocating some memory on the target process - enough to store the name of the dll
            // and storing its address in a pointer
            IntPtr allocMemAddress = VirtualAllocEx(procHandle, IntPtr.Zero, (uint)((dllPath.Length + 1) * Marshal.SizeOf(typeof(char))), MEM_COMMIT | MEM_RESERVE, PAGE_READWRITE);

            int bytesWritten;
            WriteProcessMemory(procHandle, allocMemAddress, Encoding.Default.GetBytes(dllPath), (uint)((dllPath.Length + 1) * Marshal.SizeOf(typeof(char))), out bytesWritten);

            // creating a thread that will call LoadLibraryA with allocMemAddress as argument
            CreateRemoteThread(procHandle, IntPtr.Zero, 0, loadLibraryAddr, allocMemAddress, 0, IntPtr.Zero);
        }

